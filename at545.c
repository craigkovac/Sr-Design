void udp_receive_callback(void *arg, struct udp_pcb *upcb, struct pbuf *p, const ip_addr_t *addr, u16_t port)
{
	int i, j;

	for (i = 0; i < (*p).len; ++i)
	{
		buff[i] = ((char*)(*p).payload)[i];
	}
	
	if ((*p).len >= 1)
	{
		NewPacket = buff[0];
		//NewPacket = (int)NewPacket;
		for(i = 0 ; i < (*p).len - 1 ; i++)
		{
			buff[i] = buff[i + 1];
		}
		buff[i] = NULL;
		
		for (i = 0; i < (*p).len - 1; i++)
		{
			quack = quack + buff[i] - '0';
			quack = quack * 10;
		}
		quack = (quack / 10) ;
	}
	
	pbuf_free(p);
}


